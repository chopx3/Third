package webapp;

public class Rows {

        private String row;
        private String text;
        public Rows(String Row, String Text) {
                row = Row;
                text = Text;
        }
        public String getRow() { return row; }
        public String getText() { return text; }

public void setRow(String s) { row = s; }
public void setText(String s) { text = s; }


}
